const paragraph = function (id) {

  let para = _.get(data, 'transcript.words', []),
      response = para.filter((p, i) => {
        let { name, para } = p,
            main = para.split('-')[0]
        return `p${id}` === main
      })

  return response
}

const getTotalPage = function() {
  let para = _.get(data, 'transcript.words', []),
      dataLength = para.length,
      lastId = dataLength ? dataLength - 1 : 0,
      word = _.get(para, lastId, {}),
      main = _.get(word, 'para', '').split('-')[0]

  return main.split('p')[1]

}


module.exports = function getTranscript (pageIndex = 1) {
  pageIndex = parseInt(pageIndex, 10)

  let items = paragraph(pageIndex),
      totalPage = getTotalPage()

  totalPage = parseInt(totalPage, 10)

  return {
    data: {[pageIndex]: items},
    totalPage: totalPage,
    page: pageIndex,
    itemsPerPage: items.length,
    totalItems: DATA_LENGTH,
    hasMore: pageIndex < totalPage ? true : false
  }

}

